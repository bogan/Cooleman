<div class="card card-success">
    <div class="card-header bg-default">Yabby <em>(Cherax destructor)</em></div>
    <div class="card-body">

        <img src="images/redgum.jpg" style="height:100px;float:left;padding:0px;margin: 5px"/>

        <p>
            The <a href="https://en.wikipedia.org/wiki/#"">yabby</a> is  the commonest of the freshwater crayfish in south-eastern Australia.
        </p>

        <p>
            Abundant in lowland freshwater habitats. Can survive dry conditions for long periods of time (at least several years) by aestivating (lying dormant) in burrows sunk deep into muddy creek and swamp beds.
        </p>

        <p>
            Small to medium in size, smooth-shelled. Usually brownish, but may be almost black, orange-brown, greenish or even blue.
        </p>

        <p>
            They are mainly “detritivores”, eating dead or decaying plant material. Also like meat. Breeding: usually from spring to late summer; 200-400 eggs per brood; two or three broods per season. Reproductively mature at a small size. May breed in the first year of life. Females carry fertilised eggs under their tails (“berry”).
        </p>

    </div>
</div>

