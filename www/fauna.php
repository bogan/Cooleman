<div class="card card-success">
    <div class="card-header bg-default">Australian Wood Duck <em>(Chenonetta jubata)</em></div>
    <div class="card-body">

        <img src="images/redgum.jpg" style="height:100px;float:left;padding:0px;margin: 5px"/>

        <p>
            The <a href="https://en.wikipedia.org/wiki/Australian_wood_duck">Australian Wood Duck</a> is a common waterbird.
        </p>

        <p>
            This 45–51 cm duck looks like a small goose, and feeds mostly by grazing in flocks.
        </p>

        <p>
            The male is grey with a dark brown head and mottled breast. The female has white stripes above and below the eye and mottled underparts.
        </p>

        <p>
            The Australian wood duck is widespread in Australia. It likes artificial environments such as dams, urban parks, sediment ponds, etc.
        </p>

        <p>
            These ducks are commonly found on the ridges two main working <a href="dams.php">dams</a>. The must breed there because little duckies can also be found there.
        </p>

    </div>
</div>

