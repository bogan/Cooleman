<h2>Fauna</h2>

<p>Cooleman Ridge has a surprising variety of animals. These include mammals, birds, reptiles, amphibians and insects.</p>

<p>
    The following list of animals is a featured summary of some of the more common and/or easily identified native species found on the ridge.
</p>

<p>
    The list below is intended to showcase some of our more significant animal species and provide people who are unfamiliar with the native fauna on the ridge, a basic introduction to them.
</p>

<p>
    Please see the <a href="animal-survey.php">Animal Survey</a> page for a complete list of identified species.
</p>

<div class="card card-success">
    <div class="card-header bg-default">Pink-tailed Legless Lizard <em>(Aprasia parapulchella)</em></div>
    <div class="card-body">

        <img src="images/aprsa.jpg" style="height:100px;float:left;padding:0px;margin: 5px"/>

        <p>
            The <a href="https://en.wikipedia.org/wiki/Pink-tailed_worm-lizard">Pink-tailed Legless Lizard</a> is a rare legless lizard found in Australia.

            The animal looks like a combination of small snake and worm.
        <p>

        <p>
            They are most likely to be found in rocky outcrops in native grassland on north-facing slopes of the ridge where exotic grasses have not been encouraged by application of fertiliser, and there has not been extensive grazing.
        <p>

        <p>
            When there is moisture in the soil, and before the ground has warmed above their comfort level they may be found under shallowly embedded rocks, in association with colonies of small black ants.
        </p>

        <p>
            They produce two young. Listed as vulnerable.
        </p>

        <p>
            Over the years there have been only four record sightings of them.
            University
        </p>
