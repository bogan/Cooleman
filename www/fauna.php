<div class="card card-success">
    <div class="card-header bg-default">Eastern Blue-tongued Lizard <em>(Tiliqua Tiliqua scincoides scincoides)</em></div>
    <div class="card-body">

        <img src="images/blutong.jpg" style="height:100px;float:left;padding:0px;margin: 5px"/>

        <p>
            <a href="https://en.wikipedia.org/wiki/Eastern_blue-tongued_lizard"">The Eastern blue-tongued lizard</a> is a large skink. The eastern blue-tongued lizard is a stout and slow lizard with brown to grey scales and a barred pattern across the body and tail. The underside is usually pale.
        </p>

        </p>
        They like in rocky outcrops.
        <p>

        <p>
            This reptile is not venomous, and is often found in suburban gardens where it eats snails and other invertebrates, as well as fruits and berries. It may             grow to 50 cm, and produces around a dozen live young in summer.
        </p>

        <p>
            Eastern blue tongues are quite common on the ridge and often make their way into backyards adjoining the ridge. The commonly found on the summits of many of the hills as these are typically places where large rocky outcrops are found. The are generally only observed in the warmer months when they are most active.
        </p>

