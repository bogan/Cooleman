<div class="card card-success">
    <div class="card-header bg-default">Echidna <em>(Tachyglossus aculeatus)</em></div>
    <div class="card-body">

        <img src="images/echidna.jpg" style="height:100px;float:left;padding:0px;margin: 5px"/>

        <p>
            The <a href="https://en.wikipedia.org/wiki/Echidna">Echidna</a> is a a medium-sized, solitary mammal covered with coarse hair and spines. hey are usually black or brown in colour. Their diet consists largely of ants and termites
        </p>

        <p>
            Echidnas do not tolerate extreme temperatures; they use caves and rock crevices to shelter from harsh weather conditions. Echidnas are found in forests and woodlands, hiding under vegetation, roots or piles of debris. They sometimes use the burrows of animals such as rabbits and wombats.
        </p>

        <p>
            The echidna is the only known monotreme (i.e. laying mammal) found on the ridge. Echidnas however, are quite common on the ridge and sightings are regularly reported. The most common sightings are around Kathner Street and on Mt Arawang. There have even been reports of pairs of echidnas.
        </p>
    </div>
</div>

