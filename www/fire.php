    <h2 id="fire">Fire</h2>
    
    <h3 id="management">Fire Management on Cooleman Ridge</h3>

    <p>The Ridge has always been affected by fires. Burning was introduced by Aborigines who regularly used low
        intensity fires to manage the land for their purposes.</p>

    <p>European settlers generally believed that fire was bad as it not only destroyed food but risked destroying
        infrastructure such as fences, sheds and homes. Fire was therefore generally suppressed.</p>

    <p>As the Ridge is now a nature reserve, the ACT Parks and Conservation Service adopts a number of methods to reduce
        the fuel load and manage the risk of serious fire damage:</p>

    <ul>
        <li>Regular mowing during the growing season, especially behind the houses which abut the Ridge and along the
            fire trails.
        </li>

        <li>Slashing regularly in places and on paths that the mowers cannot reach.</li>

        <li>Cattle grazing on a temporary basis and restricted to the western edge to prevent damage to native vegetation
            and the spread of exotic seeds from their hides and droppings.</li>

        <li>Control burns carried out regularly when the conditions are right. These “cool burns” reduce the ground
            litter-load but do not destroy the trees. They also help in the regeneration and proliferation of native
            flora.
        </li>
    </ul>

    <p>However, fire management on the Ridge is no match for the type of catastrophic fire that swept over it in
        2003.</p>

    <h3 id="2003">Fire on Cooleman Ridge, Saturday 18th January 2003</h3>

    <p>A lightning strike started the fire at McIntyre’s Hut in southern NSW in the week before Saturday 18 January. It
        rapidly spread and by early afternoon on the Saturday it had swept up and over the Ridge and into the
        neighbouring suburbs of Duffy, Chapman and Kambah. Four people lost their lives and over 500 houses were
        destroyed. The Ridge itself sustained extensive damage: completely blackened with not one green leaf nor blade
        of grass over the entire Ridge, a large number of dead animals and the top of the large square reservoir blown
        off. Furthermore, valuable photos and records were destroyed when Group members, Gosta and Pauline Lynga, lost
        their house.</p>

    <p>Devestated but undaunted, the Group set about restoring and regenerating the Ridge, and such work continues to
        the present day.</p>

    <p>The pictures below show Cooleman Ridge in February 2003 after the fires, and the new green growth in June 2003.
        Photo copyright: R. Vallak</p>

