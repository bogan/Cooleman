    <h2 id="history">History of the Cooleman Ridge Park Care Group (CRPCG)</h2>

    <p>The Cooleman Ridge Parkcare Group has been operating for over 30 years now. The following is a summary of the
        main periods in the groups history over the last three decades.</p>

    <h3 id="early-years">Early Years (1991 to 2001)</h3>

    <p>The CRPCG had its beginnings in May 1991 when Bess Sledge, a remarkable woman, read about the success of the
        Urambi Park Care Group, and decided to establish a similar group to restore native plant communities on Cooleman
        Ridge. Advertisements in the local press drew a group of interested volunteers, who then walked the areas with
        officers from ACT Conservation and Wildlife to assess the tasks awaiting them. An early training evening at the
        Parks depot and a ranger guided walk were both well supported.
    </p>

    <p>The first meeting of the Group took place on 29 May 1991, with a membership of 27. A letterbox drop and
        information stalls at Cooleman Court and Weston Creek Festival swelled membership to 118, of whom 34 were very
        active.</p>

    <p>September 1991 saw the first large planting exercise, when 800 eucalypts and wattles were planted above the Old
        Dam to the east with help from Greening Australia and Arawang Scouts. As cattle were still grazed on parts of
        the Ridge as a fuel-suppression measure, plantings took place in areas that had been fenced and ripped by
        Parks.</p>

    <p>Further plantings took place in 1992 on various fenced sites, with help from Greening Australia, Parks and
        Conservation staff, the Botanic Gardens and Chapman Primary School community. Plants were also being regularly
        propagated by Group members. While Bess Sledge and her husband David spent many days each week on the Ridge, a
        small band of enthusiasts formed regular working parties on Fridays and Mondays and concentrated their efforts
        on their own ‘patch’. These ‘caretaker patches’ flourished and provided rewarding work, as members saw a great
        diversity of native plant and animal life return.</p>

    <p>From early 1992 monthly Sunday work parties were also held at
        various sites that took in the length and breadth of the Ridge. From 2003 it was decided to restrict the monthly
        work parties to three major sites in order to obtain maximum benefit from these sessions.</p>

    <p> Bess Sledge’s energies extended into the community, and together with her inaugural committee she held
        fund-raising stalls, spoke to and wrote for the media, lobbied politicians, engaged with local primary schools,
        colleges and CIT and provided work for Corrective Services personnel.
    </p>

    <p>In June 1993 the Group received a major grant - the first of many over the next decade – to stabilize an erosion
        gully and fence and plant a new area. In 1994 funds were obtained to map the Ridge: it was divided into eight
        sections to help identify localized problems and assist with an ambitious tree mapping exercise.
    </p>

    <p>Regular spraying, weed removal and plantings were supplemented by guided walks along the Ridge led by Group
        members or invited specialists. Public recognition came with a Community Landcare award to the Group and an
        individual award to Bess in 1994.
    </p>

    <p>A continuing concern for the Group was the presence of cattle. Representation was made in 1995 to Canberra Nature
        Park to move all cattle and a case was put up as to why the grazing practices were inconsistent with nature
        conservation. Grazing of cattle was discontinued that year. With the removal of cattle, fire hazard risks had to be
        re-evaluated by Parks and several new tracks were made and graded to assist in fire control. The Group maintained an
        opposition to the reintroduction of cattle and began a vigorous program of brush cutting to help keep fuel loads
        down and suppress the growth of pasture weeds in areas where native vegetation was starting to recover.
    </p>

    <p>Other ongoing projects included: an herbarium and plant list, a monthly newsletter, a detailed tree survey,
        photographic records, seed collection (permits having been obtained by several members), Waterwatch activities
        at the old and new dams and research into fuel loads and fire behaviour. The enthusiasm of young children was
        harnessed through Chapman Primary School, and a second nature trail was developed within walking distance of the
        school.
    </p>

    <p>In 1996 a long-standing project of the Group finally reached fruition when the Nature Trail was officially
        opened: the culmination of three years’ planning and several months of physical labour by the Group.
    </p>

    <p>The year 2000 saw the launch of the ‘Olympic Landcare’ planting project which involved Chapman Primary School and
        many volunteers; the planting days attracted wide public and media interest.
    </p>

    <p>
        Bess Sledge’s involvement with the Ridge ended in late 2001, when family matters took her away from Canberra.
    </p>

    <h3 id="middle-years">Middle Years (2001 to 2011)</h3>

    <p>The fires of January 2003 had a devastating effect on the Ridge and on the Group who cared for it. Founding
        members Pauline and Gosta Lynga lost their house and all their possessions in the fire. Many of the Group
        records, photographs and equipment were also destroyed; and of course, the Ridge itself took the full blast of
        the firestorm.</p>

    <p>The group decided to set up more formal <a href="work#group-areas">group areas</a>.</p>

    <p>The fire recovery process began soon after, both on the Ridge and within the Group itself, with surveys of
        surviving trees. The Weston Creek Rotary Club provided help in resurrecting the landmark signs on the nature
        trail; the Southern ACT Catchment Group, of which Cooleman Ridge Park Care Group is a member, provided funds to
        replace equipment and reference books. A working bee with parents from Chapman Primary School replaced the burnt
        posts and pamphlet box on the school trail. Caretaker groups began the long task of ridding their patch of the
        many weeds that invaded the Ridge after the fires.
    </p>

    <p>In 2003 Cooleman Ridge, Farrer Ridge and Mt Taylor Park Care Groups were joint winners of the Alcoa Landcare
        Community Group award for conservation and land care work. All three groups are helping to restore biodiversity
        to former farming land in our region.</p>

    <p>A year later on 18 January 2004 a Recovery Walk took place along the Ridge with 100 people attending, and expert
        guides Joe Walker, Malcolm Gill and Daniel Iglesias providing an enlightening commentary on the impact of the
        fires.</p>

    <p>While the Group maintained their opposition to the recent resumption of cattle grazing, they agreed to help in
        2005 with data collection for Parks management into methods of ‘exotic grass control’. This experiment,
        conducted on fenced plots on the western side of the Ridge, aimed to compare the various effects of mowing, wick
        wiping and grazing on the survival of exotic grasses. The experiment was discontinued with no clear results
        achieved.</p>

    <p>In July 2006 the Group received a grant from Environment ACT to resurrect the Nature Trail which started from
    Kathner St, and has remained one of the most popular and well-used features of the Nature Park. New pamphlet
    boxes were installed, and leaflets printed as a guide to the walk. As well, a small grant from the Canberra
    Southern Cross Club in 2008 enabled the Group to print a pamphlet introducing the Park Care group, its aims and
    activities, to the general public.</p>

    <h3 id="later-years">Later Years (2011 to present)</h3>

    <p>Since 2011 the group numbers have fluctuated, but there has always been a core of committed and active members.</p>

    <p>Some projects have been:</p>

    <ul>
        <li>Tree Surveys 1997, 2005, 2009, 2013</li>
        <li>Blackberry surveys 2016, 2017</li>
        <li>Post-fire survey 2009</li>
        <li>Veg-watch monitoring since 2015</ul>
    </li>

    <p>The Group has concentrated its efforts on weeding activities to reduce the exotic invasive plants, and to
        thereby encourage natural regeneration of native species. Planting has been kept to a minimum due to the heavy
        volunteer effort to not only plant, but to maintain sufficient water supply to the seedlings to ensure
        survival.</p>





