    <h3 id="indigenous">Indigenous People</h3>

    <p>Cooleman Ridge is within an area rich in Aboriginal cultural heritage sites. However, there have been no
        significant discoveries of artifacts or cultural sites of importance on the Ridge. Most discoveries of such
        sites have been made in areas where there have been concentrations of human activity over many generations. In
        the ACT these areas are found along the river corridors, particularly at river junctions. The junction of the
        Molonglo and Murrumbidgee Rivers is particularly important, as it was a meeting place for the past 10,000 years
        of Aboriginal groups moving annually between lower altitude areas to the south and the mountain valleys of the
        Brindabella and Tindery ranges in the north.</p>

    <p>Cooleman Ridge is one of many ridges which overlooked the annual movements along the Murrumbidgee River. It is
        likely the Ridge was visited irregularly by small groups. These groups would have done much more than just
        wander at random. They would have had cultural and spiritual connections with the Ridge. Along with their
        hunting and gathering, they would have noted the condition of the vegetation and done necessary caring work.
        Frequently this would have included the lighting of fires to burn with moderate heat over small patches. It was
        this style of land management which produced the landscapes and ecologies which we are now working to recover
        and protect.</p>

    <p><cite>Mike Morriss</cite></p>

    <h3 id="european-grazing">European Settlement</h3>

    <p>The area in which Mt Arawang and the current Arawang Homestead are located was included in a grant in 1841 to
        George Weston, a former captain of the East India Company.</p>

    <p>A large property called Yarralumla was purchased in 1881 by Frederick Campbell. He turned it into the largest and
        most prosperous sheep run on the Limestone Plains. However, the landscape of the area was duly modified by
        contemporary grazing practices and by the introduction of European fauna and flora. By the end of the nineteenth
        century tree thinning and ring-barking had taken place on the property, rabbits and grasshoppers were in plague
        proportions and exotic weeds were driving out the local native flora and fauna.</p>

    <p>Early in the 1900’s the destruction of old growth timber (Stringy bark, Yellow box, Apple box) on hill tops led
        to massive soil erosion in gully areas. This was exacerbated by the grazing of sheep.</p>

    <p>After the Canberra region was chosen as the site for the new federal capital, the Commonwealth began acquiring
        land in the district. Yarralumla was one of the first properties to be acquired. The valuation of the property
        was completed in July 1913 and it was resumed by the government shortly afterwards. Much of the property later
        became the suburbs of inner Canberra.
        Cooleman, a subdivision of Yarralumla, was leased by the Champion family from 1937 until 1996 and run as a
        grazing enterprise for high-yielding merino sheep. No more clearing occurred but there was extensive grazing by
        sheep and aerial application of superphosphate and pasture seeds.</p>

    <p>In 1997 the lease on the property changed hands and the area to the west of the Ridge became an equestrian
        centre. The Ridge itself, which had been set aside as a reserve along with the 187 hectares of public land, was
        formally declared part of Canberra Nature Park in October 1993.</p>

    <p><cite>Jenny Horsfield, with revision by Linda Spinaze</cite></p>

