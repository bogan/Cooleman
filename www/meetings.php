    <h2 id="meetings">Meetings</h2>

    <p>The Cooleman Ridge Parkcare Group holds a variety of meetings that are held either on a weekly, monthly, or annual basis and which everybody is welcome to attend.</p>

    <h3 id="monthly">Monthly Meetings</h3>

    <p>
        The group holds regular monthly work meetings and are the main opportunity where members perform hands on work in accordance with our accepted <a href="strategy">strategy</a>.
    </p>

    <p>
        The meetings are normally held on the 3rd Sunday of the month and are held at one of our 6 designated <a href="work#group-areas">Group Areas</a>. The meetings in the hotter months (November to February) are held in the cool of the morning while during the cooler months (March to October) the meetings are held in the afternoon. The exact time and location is published the in the relevant months <a href="news">newsletter</a>.
    </p>

    <p>
        The main focus of each monthly meeting is <a href="weeds">weeding</a> but many other <a href="work">activities</a> (eg. surveying, photography, etc) are performed depending on the interests and abilities of the members that attend.
    </p>

    <p>
        The main objectives of the monthly meetings include the following:
    </p>

    <ul>
        <li>To work intensively on a specific <a href="work#group-areas">Group Area</a> of the ridge</li> as part of its <a href="strategy">strategy</a> to rehabilitate the ridge.
        <li>To exchange group <a href="news">news</a> and gossip</li>
        <li>To motivate members and non members</li>
        <li>To introduce new members to the group and its <a href="work">work</a></li>
        <li>To enjoy the ridge with have a cup of tea and cake!</li>
    </ul>


    <h3 id="weekly">Weekly Meetings</h3>

    <p>
        The Cooleman Ridge Parkcare Group holds smaller weekly work groups that focus on just one <a href="work#group-areas">Group Area</a> of the nature reserve.

        The weekly meetings are less formal and have a smaller attendance but perform many of the same <a href="work">activities</a> as the monthly meetings.

        The most important weekly meetings include:
    </p>

    <ul>

        <li>Kathner Group - every Friday morning at the <a href="work#group-areas">Kathner Group Area</a></li>

        <li>Namatjira Group - every Wednesday morning at the <a href="work#group-areas">Namatjira Group Area</a></li>

    </ul>

    <h3 id="annual">Annual General Meeting</h3>

    <p>
        The group holds an Annual General Meeting (AGM) for the <a href="organisation#governance">committee members</a> in accordance with its legal obligations as a incorporated organisation.
    </p>

    <p>
        The main functions performed at the Annual General Meeting include:
    </p>

    <ul>

        <li>Elect committee members for the forthcoming year.</li>

        <li>Approve official documents for submission.</li>

        <li>Perform strategic planning for the next year.</li>

    </ul>

    <p>
        The Annual General Meeting is normally held in the month of October. The exact time and location is published the months <a href="news">newsletter</a>.
    </p>

