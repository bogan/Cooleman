    <h3 id="regular">Regular Volunteer Opportunities</h3>

    <p>The Group meets on the third Sunday of each month to work on designated areas of the Ridge. These meetings are
        held in the morning during summer, and in the afternoon in autumn, winter and spring. Our work usually involves
        removing invasive exotic species by various methods. Our latest Newsletter has specific details of our next
        meeting.</p>

    <p>There is also a smaller group which meets every Friday morning near the Kathner Street Dam.</p>

    <p>Individuals are encouraged to work on chosen areas in their own time once they have gained an understanding of
        methods of weeding and are able to distinguish the different weed species.</p>

    <p>There are other ways to regularly contribute to the Group, possibly assisting with surveys or the newsletter or
        bringing some snacks to share for the work parties.</p>

    <h3 id="other">Other Volunteer Opportunities</h3>

    <p>Apart from our regular weeding parties, there are other opportunities during the year to participate in Cooleman
        Ridge activities. These include:</p>

    <ul>
        <li>Planting</li>
        <li>Studying wildlife</li>
        <li>Guided walks</li>
        <li>Tree surveys</li>
        <li>Bird monitoring</li>
        <li>Vegetation surveys</li>
        <li>Waterwatch</li>
        <li>Fire-fuel reduction</li>
        <li>Photographic recording</li>
    </ul>



