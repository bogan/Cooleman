    <h2>News</h2>

    <p>
        The Cooleman Ridge Parkcare Group publishes a monthly newsletter that contains interesting information about our group, our ridge and our work, such as:
    </p>

    <ul>
        <li><a href="meetings">Monthly Meetings</a> - previous and next</li>
        <li><a href="fauna">Fauna</a> and <a href="flora">Flora</a> sightings</li>
        <li>Other work that the group is doing</li>
        <li>General Parkcare news</li>
        <li>Features on a specific <a href="plant-list.php">plant</a> or <a href="animal-list.php">animal</a>.</li>
        <li>Trivia, gossip and interesting events that relate to Cooleman Ridge.</li>
    </ul>


    <p>
        The newsletter is published approximately 10 days before our regular <a href="meetings">monthly meeting</a> (i.e. around the 10th of each month).
    </p>

    <p>
        The newsletter is distributed by email in PDF format. If you want to subscribe or unsubscribe to the newsletter, please email <a href="mailto:lundier@iinet.net.au">Rob Lundie</a>.</p>

    <p>
        The newsletter is now also distributed in hard copy via our newsletter boxes located at strategic parts of the ridge. This targets ad hoc passer bys and those that are otherwise unaware of the groups activities.
    </p>



