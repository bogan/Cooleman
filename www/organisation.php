    <h2 id="about">About</h2>

    <p>
        The Cooleman Ridge Park Care Group is a <a href="http://www.environment.act.gov.au/parks-conservation/parks-and-reserves/get-involved/parkcare-and-urban-landcare-program">ParkCare</a> group that &quot;cares&quot; for the Cooleman Ridge Nature Reserve.
    </p>

    <p>
        The group is made up of various volunteers from a variety of backgrounds, such as scientists, public servants, homekeepers, health professionals, etc.
    </p>

    <p>
        The group was formed in 1991 by three ladies - Bess Sledge, Pauline Lynga and Tina Nevins -  who were passionate about the ridge and regenerating the bush on it. They worked hard on planting, weeding, letter writing, fundraising, recruiting, documenting, collecting, surveying, etc.
    </p>

    <p>
        These ladies have now been replaced by a new generation of volunteers who carry on their hard <a href="work">work</a> and vision.

        The group has now grown to have 50 registered members that <a href="work#contribute">contribute</a> in various ways to the groups objectives, with an active core of members that regularly <a href="meetings">meet</a> and <a href="work">work</a> on the ridge.
    </p>

    <p>
        The group is also comprised of many informal members that do not regularly attend group activities but  <a href="work#contribute">contribute</a> to the groups work by doing private work in their own way and in their own time.
    </p>

    <p>
        If you want to find out more about the group, a great resource is to read some of the group's <a href="news">newsletters</a> which provide a more detailed account of the activities of the group over the years.
    </p>

