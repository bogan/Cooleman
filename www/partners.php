<div class="col-sm-8">

    <h2>Partners and other groups</h2>

    <p>
        The group works with a number of other organisations to help care for the Cooleman Ridge Nature Reserve. These include:
    </p>

    <h3 id="parks-and-conservation">ACT Parks and Conservation Service</h3>

    <img src="images/parks.jpg" style="height:100px;width:100px;float:left;padding:5px;margin: 5px "/>

    <p>The <a target="_blank" href="http://www.environment.act.gov.au/ACT-parks-conservation">ACT Parks and Conservation Service</a> is the main government agency responsible for the daily management of the nature reserve.</p>

    <p>The ACT Parks and Conservation Service assists the group by providing information and general support to the group.</p>

    <p>They also provide rangers who regularly patrol the ridge and who often attend our meetings.</p>

    <br/>

    <h3 id="sactcg">Southern ACT Catchment Group</h3>

    <img src="images/sactcg.jpg" style="height:100px;width:100px;float:left;padding:5px;margin: 5px"/>

    <p>The <a target="_blank" href="http://sactcg.org.au/">Southern ACT Catchment Group</a> represent our group and many other active environmental groups in the southern areas of the ACT.</p>

    <p>The Southern ACT Catchment Group assists the group by helping in our development and operation, providing assistance with project ideas, funding opportunities and providing a community forum for ideas and discussion.</p>

    <br/>

    <h3 id="landcare-australia">Landcare Australia</h3>

    <img src="images/landcare.jpg" style="height:100px;width:100px;float:left;padding:5px;margin: 5px"/>

    <p><a target="_blank" href="https://landcareaustralia.org.au/">Landcare Australia</a> helps out regularly performing many hands on jobs such as weeding, spraying, planting.</p>

    <p>The Green Army assists the group by providing information and general support to the group.</p>

    <p>&nbsp;</p>

    <br/>

    <h3 id="canberra-nature-map">Canberra Nature Map</h3>

    <img src="images/canberranaturemap.jpg" style="height:100px;width:100px;float:left;padding:5px;margin: 5px"/>

    <p><a target="_blank" href="https://canberra.naturemapr.org/">Canberra Nature Map</a> is a web site run by volunteers that records sightings of various plants, animals, etc in the various nature reserves throughout the ACT and its adjoining areas.</p>

    <p>Canberra Nature Map assists the group by allowing the group to track the location of various species found on the ridge. It also complements several of our own lists such as the <a href="plant-list.php">Plant List</a> and the <a href="animal-list.php">Animal List</a>.</p>

    <br/>

    <h3 id="botanical-gardens">Australian National Botanical Gardens</h3>

    <img src="images/anbg.jpg" style="height:100px;width:100px;float:left;padding:5px;margin: 5px"/>

    <p><a target="_blank" href="https://www.anbg.gov.au/">Australian National Botanic Gardens</a> is Australia's premier scientific organisation on Australian flora.</p>

    <p>The group often liaises with the Australian National Botanic Gardens to help identify plant species found on the ridge.</p>

    <br>

    <br>

    <h3 id="water-watch">Upper Murrumbidgee Waterwatch</h3>

    <img src="images/waterwatch.webp" style="height:100px;width:200px;float:left;padding:5px;margin: 5px; background-color: rgba(1, 75, 148, 1)"/>

    <p><a target="_blank" href="https://www.act.waterwatch.org.au/">Upper Murrumbidgee Waterwatch</a> engages the community in the environment through monitoring and caring for our catchments.</p>

    <p>The group collaborates with Waterwatch to monitor the health of the dams on the ridge.</p>

    <br>

    <br>

    <h3 id="other-parkcare-groups">Other Parkcare Groups</h3>

    <p>The group often liaises with the other Parkcare groups that are located nearby. These include:</p>

