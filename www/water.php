    <h3 id="reservoirs">Reservoirs</h3>

    <p>Icon Water operates and maintains 48 reservoir sites in the four water distribution zones around Canberra. Two of
        these reservoirs are on the Ridge. One is the octagonal structure close to Darrell Place and the other is the
        round one up the hill from Guinness Place. Within the latter site is an Optus mobile telephone tower. The
        octagonal structure supplies mainly Rivett and the round one covers Chapman and upper areas of Fisher.</p>

    <p>They use a gravity feed system of distribution. Both reservoirs are supplied from the water treatment plant on Mt
        Stromlo. Some 25 pump stations distribute water around Canberra with most of it going through the Mt Stromlo
        facility which does a little ‘polishing’ or removal of microbiological organisms that may be present. Also there
        is some chlorination of water to remove contaminants that may be taken up in the journey from water treatment
        plant to householders’ taps. </p>

    <p>People may recall the water supply issues resulting from the de-roofing of the
        round reservoir and subsequent contamination of its contents during the January 2003 fire. Chapman and Fisher
        residents were advised not to drink the water. ACTEW supplied bottled water in the interim. Also, water supply
        from the octagonal reservoir was affected because power was down. A generator was brought in to maintain
        supply.</p>

    <h3 id="monitoring">Monitoring</h3>

    <p>A member of the CRPCG tests the water quality of the Kathner and Old on a monthly basis, and sends the results in to the
        <a href="https://www.act.waterwatch.org.au/">Waterwatch</a> database.</p>

    <p>This data reflects the health of the catchment area, which can be affected by changes in
        vegetation, erosion, animal activity or weather.</p>

    <h3 id="erosion">Erosion</h3>
    
    <p>Cooleman Ridge suffers from little erosion except on the main vehicle tracks, some of which are quite steep and
        therefore are prone to heavy water run-off. These tracks are repaired when funding allows.</p>

    <p>In 2007 a huge deluge on the Ridge flooded some of the houses in Chapman. As a result, the cut-drain was
        installed so that the water is safely diverted in the future.</p>


