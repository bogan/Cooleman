    <h4>Kathner St Dam</h4>

    <p>Kathner St Dam is found just inside the northern entrance.</p>

    <figure>
        <img src="/images/dam/270804.jpg" alt="Kathner St Dam" width="100%">
        <figcaption>Empty Kathner St Dam (27 August 2004)</figcaption>
    </figure>

    <p>At the same time as the Group was formed in the early 1990s, there was a drive to utilise Cooleman Ridge for
        cattle grazing for fuel reduction. The cattle would need a water supply, as would fire fighting vehicles in the
        event of a bushfire. For these purposes a dam needed to be constructed.</p>

    <p>The Group was not happy about cattle trampling native plants and spreading weeds, so they opposed the dam’s
        construction. However, as a compromise, a barbed wire fence was installed to enclose an area into which cattle
        could not enter.</p>

    <p>Over time, cattle grazing moved to another part of the Ridge where there was another dam, the Old Dam. The
        Kathner Street dam slowly filled and attracted birds, fish, frogs and yabbies. It became a feature of the Ridge
        where people could enjoy a peaceful rest on the bench listening to and watching the wildlife.</p>

    <p>So, the Group, rather than considering the dam an intrusion, developed a feeling of ownership. It decided to
        build a platform for the birds. Gösta Lyngå got the task of building it and of swimming out to anchor it in the
        middle of the dam.</p>

    <figure>
        <img src="/images/kathner-dam.jpg" alt="Kathner St Dam" width="100%">
        <figcaption>Full Kathner St Dam (11 July 2012)</figcaption>
    </figure>

    <p>A number of trees took root on the dam wall and were believed to cause water leakage. The problem was solved by
        the removal of the trees.</p>

    <p>In February 2021 a new and more comfortable seat was installed at the dam.</p>

    <p>A series of almost 300 photos of the dam between 2003 and 2012 were taken by Pauline and Gösta Lyngå.</p>

    <h4>Old Dam</h4>

    <p>Old Dam is about 1 km south of the Kathner St
        entrance on the Bicentennial Trail.</p>

    <p>This dam was included in the reserve boundaries when the reserve was first established in 1991. At that stage,
        cattle were still allowed to graze on the ridge in order to reduce fuel load, and this dam was their main source
        of water.<p/>

    <p>After the 2003 fires, intermittent cattle herds were grazed regularly along the now-fenced Bicentennial Nature
        Trail, and they continued to affect the dam with their hooves and manure. In 2018 the Parks and Conservation
        Service erected a fence around the dam to prevent cattle access, and it is now showing signs of recovery. Some
        planting has been done around the perimeter to provide habitat for birds and animals, and a new seat provides
        comfortable viewing of dam activities.</p>

